# Faça um program que leia um número inteiro e mostra na tela o seu sucessor e o seu antecessor

print('Inicio do programa!!!\n')

N = int(input('Digite um número para saber o seu sucessor e também o seu antecessor: '))

print('\nO número digitado foi {} o seu sucessor é o número {} e o seu antecessor é {} !!!'.format(N, N+1, N-1))

print('\nFim do programa!!!')
